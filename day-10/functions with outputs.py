# FUNCTIONS WITH OUTPUTS

def format_name(f_name, l_name):
    f_name = f_name.title()
    l_name = l_name.title()
    return f"{f_name} {l_name}" # AFTER THIS LINE THIS IS THE END OF THE FUNCTION

# 1st way
a = format_name("maNNy", "cArreiro")
print(a)

# 2nd way
print(format_name("maNNy", "cArreiro"))

